"""
Boolean Basics

Booleans (bool) in Python have two values: True and False
Comparison operators create boolean results
Logical operators (and, or, not) combine boolean values
"""


temp = 34

is_sunny = False
am_feeling_like_getting_wet = True


go_to_beach = temp > 28 and is_sunny or am_feeling_like_getting_wet


print("am I going to the beach?", go_to_beach)


